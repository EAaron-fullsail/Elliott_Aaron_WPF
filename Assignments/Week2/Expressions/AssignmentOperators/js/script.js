// Expressions - Assignment operators 

var a = 3; // Declare a with a value of 3
console.log("value of a :" + a); // Print value to console
var b = 4; // Declare b with a value of 4
console.log("value of a :" + b);// Print value to console
var sum = a + b; // Add a and b
console.log("value of a + b : " + sum);// Print value to console
var a = sum; // assign variable sum to a 
console.log("New value of a : " + a);// Print value to console


a -= 4; // subtract 4 from the previously stored a value
console.log("a - 4 = " + a);// Print value to console

a += 7; // add seven to that new value 
console.log("a + 7 = " + a);// Print value to console


for( i=0; i<=a; i++){ // for loop that increments a times 
	console.log(i);// Print value to console
}


for(i=a; i>=0; i--){ // for loop that decrements i times 
	console.log(i);// Print value to console
}

